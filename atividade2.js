
// 2. Qual a diferença entre declarar uma função e chamá-la? Dê um exemplo de cada.

// declarar uma função:
// É criar a função, ou seja, definir o que ela faz, mas sem executá-la ainda.
// ex1
function saudacao() {
    console.log("Olá! Seja bem-vindo!");
  }
  
//   isso só declara não executa nada ainda

// saudacao() - aqui esta chamando a funçao 

// Chamar a função:
 console.log (saudacao());
// agora sim exibira a frase da função saudacao